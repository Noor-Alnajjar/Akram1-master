package com.models;

import java.io.Serializable;

public class Category  implements Serializable {
    String id , name;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
